// C program to demonstrate working of increment and decrement operators.

#include <stdio.h>

int main() {
    int num = 5;
    printf("Initial value: %d\n", num);

    // increment operator
    num++;
    printf("After increment: %d\n", num);

    // decrement operator
    num--;
    printf("After decrement: %d\n", num);

    return 0;
}
